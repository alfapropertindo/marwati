            <h2>PROFIL</h2>
            <p>PT Diamond Alfa Propertindo didirikan pada tanggal 18 April 2019 yang pertama kali berdiri di wilayah Buton Tengah, Sulawesi Tenggara. 
PT Diamond Alfa Propetindo adalah salah satu perusahaan yang memperoduksi batuan kerikil dari ukuran 0,5 cm-3 cm yang berbeda dikawasan Desa Gundu-Gundu 
Kecamatan Mawasangka Tengah Kabupaten Buton Tengah, Provinsi Sulawesi Tenggara dengan lahan seluas 4.905 Ha.</p>
<p>PT Diamond Alfa Propertindo kegiatan utamanya berupa peledakan. Proses pemborrannya menggunakan alat bor Furukawa Rock Drill dengan pola pemborrannya
zig-zag. Bahan peledak yang digunakan adalah ANFO (Amonium Nitrat Foil Oil).PT Diamond Alfa Propertindo dalam aktivitas menerapkan penambangan terbuka
dengan metode quary dan mempunyai lokasi penambangan yang terbagi atas blok A1, A2, A3, yang saat ini dalam tahap penambangan. PT Diamond Alfa Propertindo
dalam pembongkaran batu gamping menggunakan bahan peledak ANFO untuk memisahkan batuan dari induknya karena gamping mempunyai bentuk fisik yang keras
dan struktur batuan yang bervariasi jadi peledakan adalah factor utama dalam memenuhi produksi peledakan.</p>
<p>PT Diamod Alfa Propertindo mempunyai target produk sebesar 48.000 ton/bulan. Dengan luas wilayah sebesar 4.905 Ha. Sedangkan produksi rata-rata
yang diperoleh yaitu 45.000 ton/bulan. Peta lup PT Diamond Alfa Propertindo dapat dilihat pada gambar dibawah ini. </p>
<img src="gambar/foto.jpg" alt="Gambar Lup" width="300">
<p>2.2 Keadaan Topografi Daerah</p>
<p>2.2.1 Lokasi dan Kesempatan Daerah</p>
<p>Untuk mencapai lokasi dari PT Diamond Alfa Propertindo dapat melalui beberapa alternatif perjalanan yaitu :</p>
<p>a. Jalur darat Kecamatan Tanggetada (Popalia) - Pelabuhan Boepinang dengan jarak tempuh lebih kurang 2 jam menggunakan kendaraan beroda empat.</p>
<p>b. Jalur laut dari pelabuhan Boepinang-Pelabuhan jembatan batu Bau-Bau dapat ditempuh dengan jarak lebih kurang 8 jam menggunakan kapal laut.
Kemudian dari pelabuhan jembatan batu Bau-Bau - ke pelabuhan kelurahan lakorua Kecamatan Mawasangka Tengah lebih kurang 1 jam 30 menit.</p>
<p>c. Kemudian yang terakhir, dari kelurahan lakorua ke perusahaan PT Diamond Alfa Propertindo dapat dicapai dengan jarak tempuh untuk kendaraan 
beroda dua yaitu lebih kurang 15 menit. Peta kesampaian daerah lokasi penelitian dapat dilihat pada gambar.</p>
<img src="gambar/letak.jpg" alt="Peta Kesampaian" width="300">
<p>2.2.2 Vegetasi</p>
<p>Pada lokasi penambangan batu gamping terdapat jenis tumbuh-tumbuhan tropis mulai dari semak-semak sehingga pohon yang berdiameter cukup besar
seperti pohon Manga, pohon asam, dan beberapa tumbuhanan kayu jenis lainnya yang tumbuh subur di sekitar wilayah penambangan.</p>
<p>2.3 Keadaan Geologi dan Daerah Penelitian</p>
<p>Pada kabupaten Buton Tengah tampak adanya gejala pengangkatan dari bukti adanya undak-undak terumbu karang seperti yang tampak pada unit satuan
Litostratigrafi dari Formadi Wapulaka, tetapi lapisan batuan sedimen pada umumnya horizontal tidak ada kemiringan lapisan batuan sedimen. Walaupun
demikian sekalupun terjadi peningkatan pada kuarter awal dan di akhir tersier yang tidak melibatkan pelipatan batuan, tetapi berdasarkan unsur 
geograi seperti di atas, terdapat pola kelurusan topografi dari selat Baruta sebagai bagian dari selat Buton yang paling sempit, teluk lasongko,
dan teluk wambuloli mencerminkan adanya control struktur geologi. Berdasarkan dengan dedukasi tersebut maka diketahui pola struktur geologi Kabupaten
Buton Tengah seperti diuraikan berikut ini: </p>
<p>a. Pola utara - Selatan yang diperlihatkan oleh arah Teluk Lasongko dan Teluk Wambuloli dari arah Pantai Selatan Kabupaten Buton Tengah. Arah 
kelurusan ini sama dengan arah Sesar Anjak Labusango dan Sesar Anjak Teluk Sampolawa.</p>
<p>b. Timur Laut - Barat daya dari pembelokkan Teluk Lasongko yang membelok di Wongko menuju kearah Lasongko adalah searah dengan Selat Baruta
Patahan Bungi - Langkoromi di Pulau Buton maupun Patahan Suandala yang mengarah keteluk Lawele.</p>
<p>c. Timur - Barat yang ditunjukan oleh pembelokkan Teluk Wambuloli di Katukobari yang mengarah ke Timur 
sama dengan struktur geologi Patahan Normal Pasarwajo serta Patahan Gunung Wani di Buton Utara.</p>
<p>Berdasarkan kemiripan tersebut dapat disimpulkan bahwa Litostratigrai yang berumur Tersier akhir dan Kwarter Awal di Kabupaten Buton Tengah
berada diatas bidang patahan (struktur geologi) yang berumur Miosen Tengah ke atas, sehingga pada saat terjadi pengangkatan maka bidang lemah 
yang diakibatkan oleh patahan sebelumnya menjadi tampak pada unsur geograis seperti yang diuraikan sebelumya (RPIJM Kab. Buton Tengah tahun 2016).
Peta geologi dapat dilihat pada gambar dibawah ini:</p>
<img src="gambar/geologi.jpg" alt="Peta Geologi" width="300">
<p>2.3.1 Topografi</p>
<p>Daerah penambangan batu gamping pada Diamond Alfa Propertindo Unit A1, A2, A3 merupakan rangkaian pembuktian memanjang dari uatara ke selatan
dengan tebing yang sangat terjal mempunyai ketinggian berkisar 30 m-120 m di atas permukaan air laut. Kondisi daerah tersebut merupakan perbukita 
yang ditumbuhi oleh semak belukar dan pepohonan yang tingginya antara 1-20 meter. Ketebalan lapisan tanah daerah ini antara 0,5-0,1 meter.</p>
<p>2.3.2 Stratgrafi</p>
<p>Batu gamping satuan ini umumnya dicirikan oleh batu gamping yang tidak berlapis, kristalin, keras dan kompak.Umumnya berwarna abu-abu kekuingan
hingga kuning kecoklatan. Fosil yang dikandug adalah foraminifera besar seperti Alvenolina, nummulites, disciciclina dan biplanispira yang berumur 
Eosin-Miosin bawah oleh (Soekamto 1973). Batu gamping ini tebalnya lebih kurang 20-30 meter, pada bagian atas batu gamping ini massif dengan ukuran
arenit berwarna putih dan keabu abuan muda.</p>
<p>2.3.3 Iklim dan Curah Hujan</p>
<p>Daerah Buton Tengah tergolong beriklim tropis dimana setiap tahunya dipengaruhi oleh 2 musim yaitu musim kemarau dan musim hujan. Curah hujan
dikawasan pabrik Diamond Alfa Propertindo diukur sendiri oleh perusahaan bidang seksi laboratorium.</p>
<p>2.4 Sistem Peambangan</p
<p>Sistem penambangan yang diterapkan pada penambangan batu gamping di PT Diamond Alfa Propertindo adalah sistem penambangan yang segala kegiatannya
atau aktifitasnya dilakukan diatas atau relative dekat dengan permukaan bumi, dan tempat kerjanya berhubungan lansung dengan udara luar. Metode
penambangan quarry digunakan untuk tambang permukaan dari batuan seperti Marmer, Granit dan Limestone atau quarry adalah istilah yang di pakai untuk
bahan galian golongan. </p>
