            <h2>AKTIVITAS</h2>
            <p>Aktivitas Pembongkaran Batu Gamping</p>
<p>Pembogkaran batu gamping diaksud untuk membebaskan batu gamping dari batuan induknya yang umumnya keras dan kompak (massive). Untuk melakukan
pembongkaran diperlukan alat-alat yang sesuai dan tepat dengan daerah yang akan dikerjakan. Pemilihan alat tersebut tergantung pada faktor
teknis dan ekonomis.</p>
<p>2.5.1 Perintisan (Pioneering)</p>
<p>Perintisan adalah suatu rangkaian pekerjaa yang dilakukan untuk meratakan, membuat jalan untuk dilalaui alat mekanis, serta penyediaan lokasi 
penambangan agar memudahkan pengambilan material. Pekerjaan perintisan dilakukan regu perintisan dan perencanaan tambang, setelah pekerjaan
perintisan dilaksanakan, maka pekerjaan diserahkan kepada seksi penambangan produksi untuk melanjutan pekerjaan. Adapun kegiatan yang dilakukan
didalam perintisan meliputi:</p>
<p>a. Pemborran</p>
<p>Adapun jenis mesin bor yang digunakan dalam kegiatan perintisan adalah mesin bor pneumatic. Pemboran ini dilakukan untuk pembuatan lubang ledak
horizontal dan vertikal dengan menggunakan mesin Furkawa Rock Drill (FRD) model PRC 200, yang dilengkapi dengan satu unit compressor airman PDS 750 S
dengan speed limit 20 kh/h.</p>
<p>b. Peledakan</p>
<p>Kegiatan ini dilakukan untuk membersihkan lokasi penambangan dari material hasil peledakan untuk persiapan pemboran selanjutnya, guna keperluan 
jalan penghubung dengan yang lainnya. Kegiatan pendorongan ini dilakukan dengan menggunakan alat Bulldozer satu unit.</p>
<p>2.5.2 Pembongkaraan (Loosening)</p>
<p>Pembogkaran adalah serangkaian pekerjaan yang dilakukan untuk membebaskan bahan galian dari batuan induknya. Untuk melakukan pembongkaran diperlukan
alat-alat yang sesuai dan tepat dengan daerah yang akan dikerjakan. Pemilihan alat tersebut tergantung pada factor teknis dan ekonomis.</p>
<p>a. Pemborran (drilling)</p>
<p>Pemboran adalah serangkaian pekerjaan yang dilakukan pada permukaan batuan untuk membuat lubang tembak sebagai awal dari pekerjaan 
pembongkaran batuan. Pemborran tidak dipisahkan dari rangkaian kegiatan dalam peledakan, dimana peledakan terlebih dahulu diawali dengan kegiatan 
pemborran.</p>
<p>b. Peledakan (blasting)</p>
<p>Peledakan adalah serangkaian pekerjaan terhadap batuan untuk membebaskan batuan dari batuan induknya menjadi fragmen-fragmen dengan ukuran 
yang dikehendaki.</p>
<p>2.5.3 Pendorongan (Dozing)</p>
<p>Pendorongan adalah tahap lanjut dari kegiatan peledakan yang bertujuan untuk mengumpulkan material hasil dari peledakan yang dikerjakan oleh 
alat dorong jenis Bulldozer yang selanjutnya dimuat ke alat angkut.</p>
<p>2.5.4 Pemuatan (loading)</p>
<p>Pemuatan adalah serangkaian kegiatan atau pekerjaan yang dilakukan untuk mengambil atau membuat material dari hasil peledakan kedalam alat angkut
untuk selanjutnya diangkut ketempat pengolahan (Crusher)</p>
<p>2.5.5 Pengangkutan (Hauling)</p>
<p>Pengangkutan adalah serangkaian pekerjaan yang dilakukan untuk mengangkut endapan material hasil peledakan dari ont penambangan ketempat pengolahan
atau ketempat penampungan lebih lanjut.</p>
